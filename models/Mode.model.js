import mongoose from 'mongoose';

const modeSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      trim: true,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  }
);

const Mode = mongoose.models.Mode || mongoose.model('Mode', modeSchema);

export default Mode;
